template <typename T>
void print_arr(vector<T> &arr) {
    for (T element : arr) {
        cout << element << ' ';
    }
    cout << endl;
}

template <typename T>
void sort_arr(vector<T> &arr) {
    sort(arr.begin(), arr.end());
}

void solve_testcase() {
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }

    vector<int> dp(n + 1, 0);
    dp[0] = 0;
    for (int i = 0; i < n; i++) {
        int pi = arr[i] - 1;
        dp[i + 1] = (2 * dp[i] + 2 - dp[pi] + MOD) % MOD;
    }

    cout << dp[n] << endl;
}

